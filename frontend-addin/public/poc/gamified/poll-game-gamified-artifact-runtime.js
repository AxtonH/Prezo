    '    var candidates = [0]',
    '    for (var index = 0; index < textEntries.length; index += 1) {',
    '      var entry = textEntries[index]',
    '      if (!entry || !entry.rect || !rectsOverlap(rect, entry.rect)) {',
    '        continue',
    '      }',
    '      candidates.push((entry.rect.bottom - rect.top) + gap)',
    '      candidates.push((entry.rect.top - rect.bottom) - gap)',
    '    }',
    '    candidates.sort(function (a, b) {',
    '      return Math.abs(a) - Math.abs(b)',
    '    })',
    '    var maxShift = Math.max(window.innerHeight, 1200)',
    '    for (var candidateIndex = 0; candidateIndex < candidates.length; candidateIndex += 1) {',
    '      var candidate = candidates[candidateIndex]',
    '      if (Math.abs(candidate) > maxShift) {',
    '        continue',
    '      }',
    '      var shifted = shiftRect(rect, candidate)',
    '      if (!overlapsAnyText(shifted, textEntries)) {',
    '        return candidate',
    '      }',
    '    }',
    '    var bestShift = 0',
    '    var bestArea = totalTextOverlapArea(rect, textEntries)',
    '    for (var fallbackIndex = 0; fallbackIndex < candidates.length; fallbackIndex += 1) {',
    '      var fallback = candidates[fallbackIndex]',
    '      if (Math.abs(fallback) > maxShift) {',
    '        continue',
    '      }',
    '      var shiftedRectValue = shiftRect(rect, fallback)',
    '      var area = totalTextOverlapArea(shiftedRectValue, textEntries)',
    '      if (area < bestArea) {',
    '        bestArea = area',
    '        bestShift = fallback',
    '      }',
    '    }',
    '    return bestShift',
    '  }',
    '  function clearLegacyDecorationHidden(node) {',
    '    if (!node || !node.style) {',
    '      return',
    '    }',
    '    node.style.removeProperty("opacity")',
    '    node.style.removeProperty("visibility")',
