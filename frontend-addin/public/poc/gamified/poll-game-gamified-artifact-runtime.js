    '      node.style.setProperty("position", prev.position)',
    '    } else {',
    '      node.style.removeProperty("position")',
    '    }',
    '    if (prev.zIndex) {',
    '      node.style.setProperty("z-index", prev.zIndex)',
    '    } else {',
    '      node.style.removeProperty("z-index")',
    '    }',
    '    if (prev.pointerEvents) {',
    '      node.style.setProperty("pointer-events", prev.pointerEvents)',
    '    } else {',
    '      node.style.removeProperty("pointer-events")',
    '    }',
    '    if (prev.translate) {',
    '      node.style.setProperty("translate", prev.translate)',
    '    } else {',
    '      node.style.removeProperty("translate")',
    '    }',
    '    if (prev.opacity) {',
    '      node.style.setProperty("opacity", prev.opacity)',
    '    } else {',
    '      node.style.removeProperty("opacity")',
    '    }',
    '    if (prev.visibility) {',
    '      node.style.setProperty("visibility", prev.visibility)',
    '    } else {',
    '      node.style.removeProperty("visibility")',
    '    }',
    '    delete node.__prezoDecorationDemoted',
    '  }',
    '  function setDecorationShift(node, shiftY) {',
    '    if (!node || !node.style) {',
    '      return',
    '    }',
    '    if (Math.abs(shiftY) < 0.5) {',
    '      node.style.removeProperty("translate")',
    '      return',
    '    }',
    '    node.style.setProperty("translate", "0px " + Math.round(shiftY) + "px", "important")',
    '  }',
