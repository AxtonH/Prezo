    '    if (tag === "script" || tag === "style" || tag === "noscript") {',
    '      return false',
    '    }',
    '    var text = String(node.textContent || "").trim()',
    '    return text.length > 0',
    '  }',
    '  function hasDirectRenderableText(node) {',
    '    if (!node || !node.childNodes) {',
    '      return false',
    '    }',
    '    for (var index = 0; index < node.childNodes.length; index += 1) {',
    '      var child = node.childNodes[index]',
    '      if (!child || child.nodeType !== 3) {',
    '        continue',
    '      }',
    '      if (String(child.textContent || "").trim().length > 0) {',
    '        return true',
    '      }',
    '    }',
    '    return false',
    '  }',
    '  function isThinDecorationCandidate(node, rect, style) {',
    '    if (!node || !rect || !style) {',
    '      return false',
    '    }',
    '    var tag = String(node.tagName || "").toLowerCase()',
    '    if (tag === "hr") {',
    '      return true',
    '    }',
    '    if (hasRenderableText(node)) {',
    '      return false',
    '    }',
    '    var label = (String(node.className || "") + " " + String(node.id || "")).toLowerCase()',
    '    var looksLikeDivider = /divider|ornament|separator|rule|line/.test(label)',
