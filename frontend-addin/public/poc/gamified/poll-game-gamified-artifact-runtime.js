    '    if (thin && wide) {',
    '      return true',
    '    }',
    '    return looksLikeDivider && rect.height <= 12',
    '  }',
    '  function rectsOverlap(a, b) {',
    '    if (!a || !b) {',
    '      return false',
    '    }',
    '    var horizontal = Math.min(a.right, b.right) - Math.max(a.left, b.left)',
    '    var vertical = Math.min(a.bottom, b.bottom) - Math.max(a.top, b.top)',
    '    return horizontal > 8 && vertical > 2',
    '  }',
    '  function collectTextEntries() {',
    '    var entries = []',
    '    var nodes = document.body ? document.body.querySelectorAll("*") : []',
    '    for (var index = 0; index < nodes.length; index += 1) {',
    '      var node = nodes[index]',
    '      if (!hasRenderableText(node)) {',
    '        continue',
    '      }',
    '      var style = window.getComputedStyle(node)',
    '      if (!isElementVisible(node, style)) {',
    '        continue',
    '      }',
    '      var rect = node.getBoundingClientRect()',
