    '    }',
    '    return entries',
    '  }',
    '  function shiftRect(rect, shiftY) {',
    '    if (!rect) {',
    '      return null',
    '    }',
    '    return {',
    '      left: rect.left,',
    '      right: rect.right,',
    '      top: rect.top + shiftY,',
    '      bottom: rect.bottom + shiftY,',
    '      width: rect.width,',
    '      height: rect.height',
    '    }',
    '  }',
    '  function calculateRectOverlapArea(a, b) {',
    '    if (!a || !b) {',
    '      return 0',
    '    }',
    '    var horizontal = Math.min(a.right, b.right) - Math.max(a.left, b.left)',
    '    var vertical = Math.min(a.bottom, b.bottom) - Math.max(a.top, b.top)',
    '    if (horizontal <= 0 || vertical <= 0) {',
    '      return 0',
    '    }',
    '    return horizontal * vertical',
    '  }',
    '  function overlapsAnyText(rect, textEntries) {',
    '    if (!rect || !textEntries || !textEntries.length) {',
    '      return false',
    '    }',
    '    for (var index = 0; index < textEntries.length; index += 1) {',
    '      var entry = textEntries[index]',
    '      if (!entry || !entry.rect) {',
    '        continue',
    '      }',
    '      if (rectsOverlap(rect, entry.rect)) {',
    '        return true',
    '      }',
    '    }',
    '    return false',
    '  }',
    '  function totalTextOverlapArea(rect, textEntries) {',
    '    if (!rect || !textEntries || !textEntries.length) {',
    '      return 0',
    '    }',
    '    var area = 0',
    '    for (var index = 0; index < textEntries.length; index += 1) {',
    '      var entry = textEntries[index]',
    '      if (!entry || !entry.rect) {',
    '        continue',
    '      }',
