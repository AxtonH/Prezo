    '    }',
    '  }',
    '  function scheduleArtifactSizeReport() {',
    '    if (sizeRafId) {',
    '      return',
    '    }',
    '    sizeRafId = requestAnimationFrame(function () {',
    '      sizeRafId = 0',
    '      postArtifactSize(false)',
    '    })',
    '  }',
    '  function isElementVisible(node, style) {',
    '    var computed = style || (node ? window.getComputedStyle(node) : null)',
    '    if (!node || !computed) {',
    '      return false',
    '    }',
    '    if (computed.display === "none" || computed.visibility === "hidden") {',
    '      return false',
    '    }',
    '    if (Number(computed.opacity) <= 0) {',
    '      return false',
    '    }',
    '    return true',
    '  }',
    '  function hasRenderableText(node) {',
    '    if (!node || !node.tagName) {',
    '      return false',
    '    }',
