/**
 * Bridge script lines for inline text editing and text-style commands.
 * Injected into artifact iframes via buildBridgeScript().
 *
 * Depends on bridge-scope variables set by buildBridgeScript() before these lines run:
 *   TEXT_EDIT_MESSAGE_TYPE, TEXT_STYLE_MESSAGE_TYPE, TEXT_HTML_MESSAGE_TYPE,
 *   TEXT_FOCUS_MESSAGE_TYPE, TEXT_STYLE_INIT_MESSAGE_TYPE, INSTANCE_ID,
 *   postParentMessage, invokeRenderHook
 */
export function buildTextStyleBridgeLines() {
  return [
    // ── Text-style message routing ────────────────────────────────
    '  window.addEventListener("message", function (event) {',
    '    var _msg = event && event.data',
    '    if (!_msg || typeof _msg !== "object") return',
    '    if (_msg.type === TEXT_STYLE_MESSAGE_TYPE && _msg.instanceId === INSTANCE_ID) {',
    '      applyArtifactTextStyle(_msg)',
    '    }',
    '    if (_msg.type === TEXT_STYLE_INIT_MESSAGE_TYPE && _msg.instanceId === INSTANCE_ID) {',
    '      applyArtifactStyleOverrides(_msg.overrides)',
    '    }',
    '  })',

    // ── Style application helpers ─────────────────────────────────
    '  function flattenNodeSpans(host) {',
    '    var styledEls = host.querySelectorAll("span[style], b, strong, i, em, font")',
    '    if (styledEls.length <= 1) return',
    '    // Walk all styled elements outermost→innermost; later (inner) values win per property',
    '    var all = host.querySelectorAll("[style]")',
    '    var merged = {}',
    '    for (var i = 0; i < all.length; i++) {',
    '      var s = all[i].style',
    '      if (s.fontSize) merged.fontSize = s.fontSize',
    '      if (s.fontFamily) merged.fontFamily = s.fontFamily',
    '      if (s.color) merged.color = s.color',
    '      if (s.fontWeight) merged.fontWeight = s.fontWeight',
    '      if (s.fontStyle) merged.fontStyle = s.fontStyle',
    '    }',
    '    var text = host.innerText || host.textContent || ""',
    '    var span = document.createElement("span")',
    '    if (merged.fontSize) span.style.fontSize = merged.fontSize',
    '    if (merged.fontFamily) span.style.fontFamily = merged.fontFamily',
    '    if (merged.color) span.style.color = merged.color',
    '    if (merged.fontWeight) span.style.fontWeight = merged.fontWeight',
    '    if (merged.fontStyle) span.style.fontStyle = merged.fontStyle',
    '    span.style.lineHeight = "1.2"',
    '    span.textContent = text',
    '    host.innerHTML = ""',
    '    host.appendChild(span)',
    '  }',
    '  function sendTextHtml(node) {',
    '    if (!node) return',
    '    flattenNodeSpans(node)',
    '    var field = node.getAttribute("data-prezo-editable") || "question"',
    '    var optionId = node.getAttribute("data-prezo-editable-option-id") || ""',
    '    var html = node.innerHTML || ""',
    '    postParentMessage(TEXT_HTML_MESSAGE_TYPE, { field: field, optionId: optionId, html: html })',
    '  }',
    '  function hasActiveSelection(host, sel) {',
    '    if (!sel || sel.rangeCount === 0 || sel.isCollapsed) return false',
    '    var range = sel.getRangeAt(0)',
    '    var ancestor = range.commonAncestorContainer',
    '    return host.contains(ancestor) || ancestor === host',
    '  }',
    '  function relaxContainerLayout(node) {',
    '    node.style.height = "auto"',
    '    node.style.minHeight = "0"',
    '    node.style.lineHeight = "normal"',
    '    node.style.overflow = "visible"',
    '  }',
    '  function getDeepestStyledChild(el) {',
    '    var cur = el',
    '    while (cur) {',
    '      var child = cur.querySelector("span, b, strong, i, em, font")',
    '      if (!child) break',
    '      cur = child',
    '    }',
    '    return cur || el',
    '  }',
    '  function applyStyleToAllContent(host, prop, value) {',
    '    // Read effective styles from the deepest styled child (what is actually rendered)',
    '    var deepest = getDeepestStyledChild(host)',
    '    var cs = window.getComputedStyle(deepest)',
    '    var text = host.innerText || host.textContent || ""',
    '    // Flatten all nested spans into one single clean span to prevent span accumulation',
    '    var span = document.createElement("span")',
    '    span.style.fontSize = cs.fontSize',
    '    span.style.fontFamily = cs.fontFamily',
    '    span.style.color = cs.color',
    '    span.style.fontWeight = cs.fontWeight',
    '    span.style.fontStyle = cs.fontStyle',
    '    span.style.lineHeight = "1.2"',
    '    // Override with the new property being applied',
    '    span.style[prop] = value',
    '    span.textContent = text',
    '    host.innerHTML = ""',
    '    host.appendChild(span)',
    '  }',
    '  function applyInlineStyleToSelection(host, sel, prop, value) {',
    '    var range = sel.getRangeAt(0)',
    '    // If selection covers the whole host, flatten instead of wrapping to prevent span nesting',
    '    var hostRange = document.createRange()',
    '    hostRange.selectNodeContents(host)',
    '    var isWholeHost = range.compareBoundaryPoints(Range.START_TO_START, hostRange) <= 0 &&',
    '                      range.compareBoundaryPoints(Range.END_TO_END, hostRange) >= 0',
    '    if (isWholeHost) {',
    '      applyStyleToAllContent(host, prop, value)',
    '      if (prop === "fontSize") relaxContainerLayout(host)',
    '      return',
    '    }',
    '    var span = document.createElement("span")',
    '    span.style[prop] = value',
    '    if (prop === "fontSize") { span.style.lineHeight = "1.2" }',
    '    try {',
    '      range.surroundContents(span)',
    '    } catch (e) {',
    '      var frag = range.extractContents()',
    '      span.appendChild(frag)',
    '      range.insertNode(span)',
    '    }',
    '    var newRange = document.createRange()',
    '    newRange.selectNodeContents(span)',
    '    sel.removeAllRanges()',
    '    sel.addRange(newRange)',
    '  }',

    // ── Text-style command dispatcher ─────────────────────────────
    '  function applyArtifactTextStyle(message) {',
    '    var node = activeEditNode',
    '    if (!node) return',
    '    if (activeEditNodeClearTimer) { clearTimeout(activeEditNodeClearTimer); activeEditNodeClearTimer = null }',
    '    var doc = node.ownerDocument || document',
    '    var win = doc.defaultView || window',
    '    var sel = win.getSelection ? win.getSelection() : null',
    '    var styleCmd = message.styleCmd',
    '    var styleValue = message.styleValue',
    '    var hasSelection = hasActiveSelection(node, sel)',
    '    if (styleCmd === "clear") {',
    '      node.innerHTML = node.innerText || node.textContent || ""',
    '      node.removeAttribute("style")',
    '      sendTextHtml(node)',
    '      sendTextEdit(node)',
    '      reportNodeStyles(node)',
    '      return',
    '    }',
    '    if (styleCmd === "bold") {',
    '      if (hasSelection) { doc.execCommand("bold", false, null) }',
    '      else {',
    '        var curWeight = window.getComputedStyle(getDeepestStyledChild(node)).fontWeight',
    '        var isBold = curWeight === "bold" || curWeight === "700" || parseInt(curWeight, 10) >= 700',
    '        applyStyleToAllContent(node, "fontWeight", isBold ? "normal" : "bold")',
    '      }',
    '    } else if (styleCmd === "italic") {',
    '      if (hasSelection) { doc.execCommand("italic", false, null) }',
    '      else {',
    '        var curStyle = window.getComputedStyle(getDeepestStyledChild(node)).fontStyle',
    '        applyStyleToAllContent(node, "fontStyle", curStyle === "italic" ? "normal" : "italic")',
    '      }',
    '    } else if (styleCmd === "fontSize" && styleValue) {',
    '      if (hasSelection) { applyInlineStyleToSelection(node, sel, "fontSize", styleValue) }',
    '      else { applyStyleToAllContent(node, "fontSize", styleValue) }',
    '      relaxContainerLayout(node)',
    '    } else if (styleCmd === "fontFamily" && styleValue) {',
    '      if (hasSelection) { applyInlineStyleToSelection(node, sel, "fontFamily", styleValue) }',
    '      else { applyStyleToAllContent(node, "fontFamily", styleValue) }',
    '      relaxContainerLayout(node)',
    '    } else if (styleCmd === "color" && styleValue) {',
    '      if (hasSelection) { applyInlineStyleToSelection(node, sel, "color", styleValue) }',
    '      else { applyStyleToAllContent(node, "color", styleValue) }',
    '    }',
    '    sendTextHtml(node)',
    '    sendTextEdit(node)',
    '    reportNodeStyles(node)',
    '  }',

    // ── Style-override restore (on render) ────────────────────────
    '  function findEditableNode(field, optionId) {',
    '    var nodes = document.body ? document.body.querySelectorAll("[data-prezo-editable]") : []',
    '    for (var i = 0; i < nodes.length; i++) {',
    '      var n = nodes[i]',
    '      if (n.getAttribute("data-prezo-editable") === field) {',
    '        if (!optionId || n.getAttribute("data-prezo-editable-option-id") === optionId) {',
    '          return n',
    '        }',
    '      }',
    '    }',
    '    return null',
    '  }',
    '  function applyArtifactStyleOverrides(overrides) {',
    '    if (!overrides || typeof overrides !== "object") return',
    '    var keys = Object.keys(overrides)',
    '    for (var k = 0; k < keys.length; k++) {',
    '      var nodeKey = keys[k]',
    '      var html = overrides[nodeKey]',
    '      if (typeof html !== "string" || !html) continue',
    '      var parts = nodeKey.split(":")',
    '      var field = parts[0]',
    '      var optionId = parts[1] || ""',
    '      var node = findEditableNode(field, optionId)',
    '      if (!node) continue',
    '      node.innerHTML = html',
    '      flattenNodeSpans(node)',
    '      if (html.indexOf("font-size") !== -1 || html.indexOf("font-family") !== -1) {',
    '        relaxContainerLayout(node)',
    '      }',
    '    }',
    '  }',

    // ── Inline text editing — node scanning & event handlers ──────
    '  var TEXT_EDIT_SELECTORS = [',
    '    ".label", ".option-label", ".poll-label", ".name",',
    '    "[data-option-label]", "[data-prezo-option-label]", "[data-label]",',
    '    ".question", ".poll-question", ".title", ".poll-title",',
    '    "[data-question]", "[data-poll-question]",',
    '    ".poll-subtitle", ".subtitle", ".sub-title",',
    '    ".total-votes", ".poll-footer", ".poll-total",',
    '    "#totalVotes", "#total-votes",',
    '    "h1", "h2", "h3"',
    '  ].join(", ")',
    '  var SKIP_EDIT_SELECTORS = "script, style, svg, canvas, img, video, picture, .track, .bar, .stat-votes, .stats, [data-votes], [data-percentage], [data-prezo-runtime-background-layer]"',
    '  var editableNodes = new Set()',
    '  var editStyleInjected = false',
    '  function injectEditStyles() {',
    '    if (editStyleInjected) return',
    '    editStyleInjected = true',
    '    var s = document.createElement("style")',
    '    s.textContent = [',
    '      "[data-prezo-editable] { cursor: text; transition: outline 0.15s, box-shadow 0.15s; border-radius: 2px; }",',
    '      "[data-prezo-editable]:hover { outline: 1.5px dashed rgba(74,124,255,0.5); outline-offset: 2px; }",',
    '      "[data-prezo-editable]:focus { outline: 2px solid rgba(74,124,255,0.85); outline-offset: 2px; box-shadow: 0 0 0 3px rgba(74,124,255,0.18); }",',
    '      "[data-prezo-editable][contenteditable=true] { -webkit-user-modify: read-write; }",',
    '      "[data-prezo-editable]:empty::before { content: attr(data-prezo-placeholder); opacity: 0.4; pointer-events: none; }"',
    '    ].join("\\n")',
    '    ;(document.head || document.documentElement).appendChild(s)',
    '  }',
    // ── Heuristic subtitle / footer detection ────────────────────
    '  function isLikelySubtitleNode(node) {',
    '    if (!node || node.nodeType !== 1) return false',
    '    var explicit = node.getAttribute && node.getAttribute("data-prezo-editable")',
    '    if (explicit === "subtitle") return true',
    '    var id = (node.id || "").toLowerCase()',
    '    var cls = (node.className || "").toLowerCase()',
    '    if (cls.indexOf("poll-subtitle") !== -1 || cls.indexOf("subtitle") !== -1 || cls.indexOf("sub-title") !== -1) return true',
    '    if (id.indexOf("subtitle") !== -1 || id.indexOf("sub-title") !== -1) return true',
    '    return false',
    '  }',
    '  function isLikelyFooterNode(node) {',
    '    if (!node || node.nodeType !== 1) return false',
    '    var explicit = node.getAttribute && node.getAttribute("data-prezo-editable")',
    '    if (explicit === "footer") return true',
    '    var id = (node.id || "").toLowerCase()',
    '    var cls = (node.className || "").toLowerCase()',
    '    if (cls.indexOf("total-votes") !== -1 || cls.indexOf("poll-footer") !== -1 || cls.indexOf("poll-total") !== -1) return true',
    '    if (id === "totalvotes" || id === "total-votes" || id.indexOf("totalvotes") !== -1) return true',
    '    var text = (node.innerText || node.textContent || "").trim().toLowerCase()',
    '    if (/^\\d+\\s+total\\s+vote/i.test(text)) return true',
    '    return false',
    '  }',

    // ── Unified node classification ─────────────────────────────
    '  function classifyEditableNode(node) {',
    '    if (!node || node.nodeType !== 1) return null',
    '    if (node.matches && node.matches(SKIP_EDIT_SELECTORS)) return null',
    '    if (node.closest && node.closest(SKIP_EDIT_SELECTORS)) return null',
    '    var explicit = node.getAttribute && node.getAttribute("data-prezo-editable")',
    '    if (explicit === "subtitle") return { field: "subtitle", optionId: "" }',
    '    if (explicit === "footer") return { field: "footer", optionId: "" }',
    '    if (isLikelySubtitleNode(node)) return { field: "subtitle", optionId: "" }',
    '    if (isLikelyFooterNode(node)) return { field: "footer", optionId: "" }',
    '    var optionRow = node.closest ? node.closest("[data-option-id], [data-prezo-option-id], .option, .poll-option, .option-row, .poll-row, .choice, .answer") : null',
    '    if (optionRow) {',
    '      var optionId = ""',
    '      if (optionRow.dataset) optionId = optionRow.dataset.optionId || optionRow.dataset.prezoOptionId || ""',
    '      if (!optionId) {',
    '        var siblings = optionRow.parentElement ? Array.prototype.slice.call(optionRow.parentElement.children) : []',
    '        optionId = "option-" + siblings.indexOf(optionRow)',
    '      }',
    '      return { field: "option-label", optionId: optionId }',
    '    }',
    '    return { field: "question", optionId: "" }',
    '  }',
    '  var COPY_FIELD_PLACEHOLDERS = { question: "Enter question…", "option-label": "Enter option…", subtitle: "Enter subtitle…", footer: "Enter footer…" }',
    '  function makeNodeEditable(node) {',
    '    if (!node || editableNodes.has(node)) return',
    '    var classification = classifyEditableNode(node)',
    '    if (!classification) return',
    '    editableNodes.add(node)',
    '    node.setAttribute("data-prezo-editable", classification.field)',
    '    if (classification.optionId) node.setAttribute("data-prezo-editable-option-id", classification.optionId)',
    '    node.setAttribute("data-prezo-placeholder", COPY_FIELD_PLACEHOLDERS[classification.field] || "Edit…")',
    '    node.setAttribute("contenteditable", "true")',
    '    node.addEventListener("focus", handleEditFocus)',
    '    node.addEventListener("blur", handleEditBlur)',
    '    node.addEventListener("input", handleEditInput)',
    '    node.addEventListener("keydown", handleEditKeydown)',
    '    node.addEventListener("paste", handleEditPaste)',
    '  }',
    '  var suppressNextRender = false',
    '  var activeEditNode = null',
    '  var activeEditNodeClearTimer = null',
    '  function reportNodeStyles(node) {',
    '    var field = node.getAttribute("data-prezo-editable") || "question"',
    '    var optionId = node.getAttribute("data-prezo-editable-option-id") || ""',
    '    var styleTarget = getDeepestStyledChild(node)',
    '    var computed = window.getComputedStyle(styleTarget)',
    '    postParentMessage(TEXT_FOCUS_MESSAGE_TYPE, {',
    '      field: field, optionId: optionId, active: true,',
    '      color: computed.color || "",',
    '      fontSize: computed.fontSize || "",',
    '      fontFamily: computed.fontFamily || ""',
    '    })',
    '  }',
    '  function handleEditFocus(e) {',
    '    var node = e.target',
    '    if (activeEditNodeClearTimer) { clearTimeout(activeEditNodeClearTimer); activeEditNodeClearTimer = null }',
    '    activeEditNode = node',
    '    suppressNextRender = true',
    '    var field = node.getAttribute("data-prezo-editable") || "question"',
    '    var optionId = node.getAttribute("data-prezo-editable-option-id") || ""',
    '    var styleTarget = getDeepestStyledChild(node)',
    '    var computed = window.getComputedStyle(styleTarget)',
    '    postParentMessage(TEXT_FOCUS_MESSAGE_TYPE, {',
    '      field: field,',
    '      optionId: optionId,',
    '      active: true,',
    '      color: computed.color || "",',
    '      fontSize: computed.fontSize || "",',
    '      fontFamily: computed.fontFamily || ""',
    '    })',
    '  }',
    '  function handleEditBlur(e) {',
    '    var node = e.target',
    '    setTimeout(function () { suppressNextRender = false }, 100)',
    '    sendTextEdit(node)',
    '    sendTextHtml(node)',
    '    var field = node.getAttribute("data-prezo-editable") || "question"',
    '    postParentMessage(TEXT_FOCUS_MESSAGE_TYPE, { field: field, active: false })',
    '    // Keep activeEditNode alive for 2s so toolbar style commands can still target it',
    '    activeEditNodeClearTimer = setTimeout(function () {',
    '      activeEditNodeClearTimer = null',
    '      activeEditNode = null',
    '    }, 2000)',
    '  }',
    '  function handleEditInput(e) {',
    '    sendTextEdit(e.target)',
    '  }',
    '  function handleEditKeydown(e) {',
    '    if (e.key === "Enter") {',
    '      e.preventDefault()',
    '      e.target.blur()',
    '    }',
    '    if (e.key === "Escape") {',
    '      e.preventDefault()',
    '      e.target.blur()',
    '    }',
    '  }',
    '  function handleEditPaste(e) {',
    '    e.preventDefault()',
    '    var text = (e.clipboardData || window.clipboardData || { getData: function () { return "" } }).getData("text/plain")',
    '    if (text) document.execCommand("insertText", false, text)',
    '  }',
    '  function sendTextEdit(node) {',
    '    if (!node) return',
    '    var field = node.getAttribute("data-prezo-editable") || "question"',
    '    var optionId = node.getAttribute("data-prezo-editable-option-id") || ""',
    '    var text = (node.innerText || node.textContent || "").replace(/\\n/g, " ").trim()',
    '    postParentMessage(TEXT_EDIT_MESSAGE_TYPE, { field: field, optionId: optionId, text: text })',
    '  }',
    '  function scanAndEnableEditing() {',
    '    injectEditStyles()',
    '    if (!document.body) return',
    '    var candidates = document.body.querySelectorAll(TEXT_EDIT_SELECTORS)',
    '    for (var i = 0; i < candidates.length; i++) {',
    '      var node = candidates[i]',
    '      var isCopyField = isLikelySubtitleNode(node) || isLikelyFooterNode(node)',
    '      var maxChildren = isCopyField ? 6 : 3',
    '      var maxTextLen = isCopyField ? 800 : 500',
    '      if (node.children && node.children.length > maxChildren) continue',
    '      var text = (node.innerText || node.textContent || "").trim()',
    '      if (!text || text.length > maxTextLen) continue',
    '      makeNodeEditable(node)',
    '    }',
    '  }',
    // ── Post-render copy reapplication ────────────────────────────
    '  function applyArtifactCopyFromMeta(payload) {',
    '    var copy = payload && payload.meta && payload.meta.artifactCopy',
    '    if (!copy || typeof copy !== "object") return',
    '    if (!document.body) return',
    '    var subtitleText = typeof copy.subtitle === "string" ? copy.subtitle : null',
    '    var footerSuffix = typeof copy.footerSuffix === "string" ? copy.footerSuffix : null',
    '    var footerLegacy = typeof copy.footer === "string" ? copy.footer : null',
    '    var totalVotes = payload.totalVotes',
    '    if (typeof totalVotes !== "number" || !isFinite(totalVotes)) totalVotes = 0',
    '    if (subtitleText !== null) {',
    '      var subtitleNodes = document.body.querySelectorAll("[data-prezo-editable=subtitle]")',
    '      for (var si = 0; si < subtitleNodes.length; si++) {',
    '        subtitleNodes[si].textContent = subtitleText',
    '      }',
    '    }',
    '    if (footerSuffix !== null || footerLegacy !== null) {',
    '      var suffix = footerSuffix !== null ? footerSuffix : footerLegacy',
    '      var footerNodes = document.body.querySelectorAll("[data-prezo-editable=footer]")',
    '      for (var fi = 0; fi < footerNodes.length; fi++) {',
    '        var splitCount = footerNodes[fi].querySelector("[data-prezo-poll-total-count]")',
    '        if (splitCount) {',
    '          var textSibling = splitCount.nextSibling',
    '          if (textSibling && textSibling.nodeType === 3) textSibling.textContent = " " + suffix',
    '        } else {',
    '          footerNodes[fi].textContent = String(totalVotes) + " " + suffix',
    '        }',
    '      }',
    '    }',
    '  }',
    '  function applyArtifactCopyInit() {',
    '    var pollState = window.__PREZO_POLL_STATE',
    '    if (pollState) applyArtifactCopyFromMeta(pollState)',
    '  }',

    // Patch invokeRenderHook to suppress re-renders while user is editing
    // and to reapply copy overrides + enable editing after each render
    '  var _originalInvokeRenderHook = invokeRenderHook',
    '  invokeRenderHook = function (payload) {',
    '    if (suppressNextRender && activeEditNode) {',
    '      return',
    '    }',
    '    _originalInvokeRenderHook(payload)',
    '    applyArtifactCopyFromMeta(payload)',
    '    setTimeout(scanAndEnableEditing, 60)',
    '  }',
    // Initial scan + copy application after first render + DOMContentLoaded
    '  setTimeout(function () { applyArtifactCopyInit(); scanAndEnableEditing() }, 200)',
    '  document.addEventListener("DOMContentLoaded", function () { setTimeout(function () { applyArtifactCopyInit(); scanAndEnableEditing() }, 200) })',
  ]
}
